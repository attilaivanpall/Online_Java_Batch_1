
public class DeclaringPrimitiveVariables {

	
	
	
	public static void main(String[] args) {
		
		byte b = -128;
		
		short s = 100;
		
		int i = 100000;
		
		long l = 12345678901l;
		
		long var = 100;
		
		float var1 = 100;
		
		System.out.println(var1); //100.0
		
		float f = 100.50234f;
		
		double var2 = 100.50;
		
		char c = 'a';
		
		char c1 = 100;
		
		System.out.println(c1);
		
		boolean var11 = false;
		
	}
	
}
